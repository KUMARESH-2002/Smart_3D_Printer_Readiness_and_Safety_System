void DHT11_Start (void)
{
    Set_Pin_Output (DHT11_PORT, DHT11_PIN);  // set the pin as output
	HAL_GPIO_WritePin (DHT11_PORT, DHT11_PIN, 0);   // pull the pin low
	HAL_Delay (18);   // wait for 18ms
	HAL_GPIO_WritePin (DHT11_PORT, DHT11_PIN, 1);
	delay (20);// set as input
	Set_Pin_Input (DHT11_PORT, DHT11_PIN);
}
uint8_t DHT11_Check_Response (void)
{
	uint8_t Response = 0;
	delay (40);
	if (!(HAL_GPIO_ReadPin (DHT11_PORT, DHT11_PIN)))
	{
		delay (80);
		if ((HAL_GPIO_ReadPin (DHT11_PORT, DHT11_PIN))) Response = 1;
		else Response = -1;
	}
	while ((HAL_GPIO_ReadPin (DHT11_PORT, DHT11_PIN)));   // wait for the pin to go low

	return Response;
}
uint8_t DHT11_Read (void)
{
	uint8_t i,j;
	for (j=0;j<8;j++)
	{
		while (!(HAL_GPIO_ReadPin (DHT11_PORT, DHT11_PIN)));   // wait for the pin to go high
		delay (40);   // wait for 40 us
		if (!(HAL_GPIO_ReadPin (DHT11_PORT, DHT11_PIN)))   // if the pin is low
		{
			i&= ~(1<<(7-j));   // write 0
		}
		else i|= (1<<(7-j));  // if the pin is high, write 1
		while ((HAL_GPIO_ReadPin (DHT11_PORT, DHT11_PIN)));  // wait for the pin to go low
	}
	return i;
}

void check_vibration(void) {
    static uint32_t debounce_time = 50;  // Debounce time in ms
    static uint32_t last_vibration_time = 0;

    if (HAL_GPIO_ReadPin(VIBRATION_PORT, VIBRATION_PIN) == GPIO_PIN_RESET) {
        if (HAL_GetTick() - last_vibration_time > debounce_time) {
            vibration_detected = 1;
            last_vibration_time = HAL_GetTick();
        }
    }

    if (vibration_detected == 1) {
        HAL_GPIO_WritePin(BUZZER_PORT, BUZZER_PIN, RESET);  // Turn ON buzzer
        vibration_detected = 0;  // Reset flag
    } else {
        HAL_GPIO_WritePin(BUZZER_PORT, BUZZER_PIN, SET);  // Turn OFF buzzer
    }
}
